word = str(input("Введіть слово: ")) # введення слова

while not word.isalpha(): # якщо введенно не слово
    word = input("Помилка, введено не слово\nСпробуйте знову: ") # повторне введення

sum_ascii = 0 # оголошення змінної для суми

for i in range (len(word)): # цикл по символам слова
    sum_ascii += ord(word[i]) # обрахунок суми ASCII кодів символів слова

print("Сума ASCII-кодів символів слова: ", sum_ascii) # виведення результатів

