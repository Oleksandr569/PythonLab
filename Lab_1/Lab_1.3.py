n = int(input("Введіть n (1<n<9), де n це висота рисунка: ")) # введення n

while (n < 2 or n > 8): # якщо n введенно не вірно
    print("ПОМИЛКА: n повинно бути більшим за 1 та меншим за 9")  # повідомлення про помилку
    n = int(input("Введіть n (1<n<9), де n це висота рисунка: ")) # повторне введення n

for i in range(1, n + 1): # зовнішній цикл
    for j in range(i, 0, -1): # внутрішній цикл
        print(j, end = " ") # виведення елементів
    print("") # для початку з нового рядка
